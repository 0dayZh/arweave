-module(ar_tx_blacklist_sup).

-behaviour(supervisor).

%%%===================================================================
%%% API
%%%===================================================================

-export([start_link/0]).

%%%===================================================================
%%% Callbacks
%%%===================================================================

-export([init/1]).

%%%===================================================================
%%% Public API
%%%===================================================================

start_link() ->
	supervisor:start_link({local, ?MODULE}, ?MODULE, []).

%%%===================================================================
%%% Supervisor callbacks
%%%===================================================================

init(Args) ->
	SupFlags = #{strategy => one_for_one, intensity => 10, period => 1},
	ChildSpec = #{id => ar_tx_blacklist, start => {ar_tx_blacklist, start_link, [Args]}},
	{ok, {SupFlags, [ChildSpec]}}.
